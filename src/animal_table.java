    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel)
        t1.getModel();
        try
        {
            Class.forName("java.sql.DriverManager");
            Connection con = (Connection)
            DriverManager.getConnection("jdbc:mysql://localhost:3306/zoo","root", "1234");
            Statement stmt = (Statement) con.createStatement();
            String query="SELECT * FROM animal;";
            ResultSet rs = stmt.executeQuery(query);
            while(rs.next())
            {
                String id = rs.getString("Animal_id");
                String name = rs.getString("DOB");
                String bg=rs.getString("Name");
                String contact=rs.getString("Category");
                String DOJ=rs.getString("Source");
               
                String gender=rs.getString("Gender");
                model.addRow (new Object[] {id,name,bg,contact,DOJ,gender});
            }
            
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog (this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

